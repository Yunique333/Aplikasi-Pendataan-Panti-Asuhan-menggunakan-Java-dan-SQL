    private void tmblEditActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_tmblEditActionPerformed
        // TODO add your handling code here:
        try{
            int index = DataAdmin.tabel_data_admin.getSelectedRow();
            TableModel  model= DataAdmin.tabel_data_admin.getModel();
            String nm=model.getValueAt(index, 0).toString();
            
            Connection conn = DriverManager.getConnection("jdbc:mysql://localhost:3306/panti_asuhan","root","");
            Statement stat=conn.createStatement();
            String sql="UPDATE `tabel_admin` SET `nama`='%s',`username`='%s',`password`='%s' WHERE `nama`='%s'";
            sql = String.format(sql,nama.getText(),username.getText(),password.getText(),nm);
            if(stat.executeUpdate(sql)>=1){              
                JOptionPane.showMessageDialog(null,"ANDA BERHASIL EDIT DATA","PEMBERITAHUAN",JOptionPane.INFORMATION_MESSAGE); 
            }
            else{
                JOptionPane.showMessageDialog(null,"MAAF ANDA TIDAK BISA EDIT DATA","PEMBERITAHUAN",JOptionPane.INFORMATION_MESSAGE);
            }
            kosongkan_form();
        }catch(SQLException e){
            JOptionPane.showMessageDialog(this, e.getMessage());
            //System.out.println("Error : "+ e.getMessage());
        }
        
    }//GEN-LAST:event_tmblEditActionPerformed


